package main;

import java.io.Serializable;

/**
 *
 * @author Darek
 */
public class Horse implements Serializable {
    private int ID;
    private int userID;
    private String name;
    private String image;
    
public Horse (int ID, int userID, String name, String image){
    this.ID = ID;
    this.name = name;
    this.image = image;
    this.userID = userID;
}

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
}

    




